def char_data(ch):
    """Return 8-byte row-format bitmap for a Cyrillic char, or None."""
    cp = ord(ch)
    if 0x0410 <= cp <= 0x042F:      # А-Я
        idx = cp - 0x0410
    elif 0x0430 <= cp <= 0x044F:    # а-я → same glyphs as А-Я
        idx = cp - 0x0430
    else:
        return None
    off = idx << 3  # idx * 8
    return _FONT[off:off + 8]
